fn eval(op: OpCode, regs: [usize; 4], a: usize, b: usize) -> usize {
    match op {
        Addr => regs[a] + regs[b],
        Addi => regs[a] + b,
        Mulr => regs[a] * regs[b],
        Muli => regs[a] * b,
        Banr => regs[a] & regs[b],
        Bani => regs[a] & b,
        Borr => regs[a] | regs[b],
        Bori => regs[a] | b,
        Setr => regs[a],
        Seti => a,
        Gtir => if a > regs[b] { 1 } else { 0 },
        Gtri => if regs[a] > b { 1 } else { 0 },
        Gtrr => if regs[a] > regs[b] { 1 } else { 0 },
        Eqir => if a == regs[b] { 1 } else { 0 },
        Eqri => if regs[a] == b { 1 } else { 0 },
        Eqrr => if regs[a] == regs[b] { 1 } else { 0 },
    }
}

fn could_be(i: usize, op: OpCode) -> bool {
    let mut regs = EXAMPLES[i].before;
    let [_, a, b, c] = EXAMPLES[i].instr;
    let after = EXAMPLES[i].after;

    regs[c] = eval(op, regs, a, b);

    regs == after
}

fn main() {
    let mut three_or_more = 0;

    for i in 0..EXAMPLES.len() {
        let mut count = 0;

        for op in ALL_CODES.iter() {
            if could_be(i, *op) {
                count += 1;
            }
        }

        if count >= 3 {
            three_or_more += 1;
        }
    }

    println!("Part 1: {}", three_or_more);

    // Start with all the possibility that all opcodes could be all instructions
    let mut possibilities = Vec::new();
    for i in 0..16 {
        possibilities.push(HashSet::new());

        for j in 0..16 {
            possibilities[i].insert(ALL_CODES[j]);
        }
    }

    // Filter out all possibilities that don't match the examples
    for i in 0..EXAMPLES.len() {
        let numeral_op = EXAMPLES[i].instr[0];
        let codes = possibilities[numeral_op].clone();

        for op in &codes {
            if !could_be(i, *op) {
                possibilities[numeral_op].remove(op);
            }
        }
    }

    // Decide the final mapping
    loop {
        let mut singletons = HashSet::new();
        for i in 0..possibilities.len() {
            let codes = &possibilities[i];
            if codes.len() == 1 {
                let op = codes.iter().nth(0).unwrap();
                singletons.insert(*op);
            }
        }

        if singletons.len() == 16 {
            break
        }

        for i in 0..possibilities.len() {
            if possibilities[i].len() > 1 {
                possibilities[i].retain(|code| !singletons.contains(code));
            }
        }
    }

    let instruction_mapping: Vec<_> =
        possibilities.iter().map(|set| *set.iter().nth(0).unwrap()).collect();

    let mut regs = [0, 0, 0, 0];
    for instr in PROGRAM.iter() {
        let [op, a, b, c] = instr;
        regs[*c] = eval(instruction_mapping[*op], regs, *a, *b);
    }
    println!("Part 2: {}", regs[0]);
}

