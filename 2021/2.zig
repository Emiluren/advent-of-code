    var depth: i32 = 0;

    for (input) |cmd| {
        switch (cmd.dir) {
            Dir.forward => {
                hor += cmd.amount;
            },
            Dir.down, => {
                depth += cmd.amount;
            },
            Dir.up, => {
                depth -= cmd.amount;
            }
        }
    }

    std.debug.print("Part 1 {d}\n", .{hor * depth});
}

